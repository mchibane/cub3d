#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	i;

	str = ft_strdup(s);
	if (!str)
		return (NULL);
	i = 0;
	while (s && str[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	return (str);
}
